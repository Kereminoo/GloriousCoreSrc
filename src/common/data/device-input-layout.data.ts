export class LayoutLine {
    x?: number = 0;
    y?: number = 0;
    length: number = 0;
    angle?: number;
}

export class LayoutNode {
    translationKey: string;
    isKey: boolean = true;
    isRotary: boolean = false;
    isSlider: boolean = false;
    isButton: boolean = false;
    hasLight: boolean = true;
    canBind: boolean = true;
    // canSetLighting: boolean = true; // some lighting is only for batteries. shouldn't render if it can't be set, but this might be useful?
    position: { x: number; y: number } = { x: 0, y: 0 };
    size?: { width: number; height: number };
    lines?: LayoutLine[] = [];
    terminationPoint?: { x: number; y: number } = { x: 0, y: 0 };
    lineSide: 'left' | 'right' = 'right';
    keybindArrayIndex: number = -1;
    hideUI: boolean = false;
    quickKeyIds: QuickKeyId[] = [QuickKeyId.All];

    constructor(translationKey: string, options?: Partial<LayoutNode>) {
        this.translationKey = translationKey;

        Object.assign(this, options);

        if (!this.isKey && !this.isRotary && !this.isSlider && !this.isButton) {
            this.canBind = false;
        }
    }
}

